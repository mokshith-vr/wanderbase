export function filterJobs(params: {
  stack?: string;
  min_salary?: number;
  india_friendly?: boolean;
  page: number;
  limit: number;
}): { jobs: Job[]; total: number } {
  let filtered = [...JOBS];

  if (params.stack) {
    const stacks = params.stack.toLowerCase().split(",");
    filtered = filtered.filter((job) =>
      stacks.some((s) =>
        job.tech_stack.some((t) => t.toLowerCase().includes(s.trim()))
      )
    );
  }

  if (params.min_salary !== undefined) {
    filtered = filtered.filter(
      (job) => job.salary_min_usd !== null && job.salary_min_usd >= params.min_salary!
    );
  }

  if (params.india_friendly !== undefined) {
    filtered = filtered.filter(
      (job) => job.india_friendly === params.india_friendly
    );
  }

  // Featured jobs first
  filtered.sort((a, b) => {
    if (a.is_featured && !b.is_featured) return -1;
    if (!a.is_featured && b.is_featured) return 1;
    return new Date(b.posted_at).getTime() - new Date(a.posted_at).getTime();
  });

  const total = filtered.length;
  const start = (params.page - 1) * params.limit;
  const jobs = filtered.slice(start, start + params.limit);

  return { jobs, total };
}
